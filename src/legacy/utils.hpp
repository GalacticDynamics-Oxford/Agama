inline double GaussLegendreQuad(double (*func)(double, void*),double a,double b, void *p = NULL){
	// Adapted from numerical recipes routine qgaus
	double xm=0.5*(b+a);
	double xr=0.5*(b-a);
	double ss=0.0;
	for (int j=0; j<50; j++){
		double dx=xr*abscissa50[j];
		ss=ss+weights50[j]*(func(xm+dx,p)+func(xm-dx,p));
	}
	return ss*xr;
}

